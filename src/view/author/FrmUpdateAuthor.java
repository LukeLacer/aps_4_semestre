    private void listAuthors(){
        DefaultTableModel tableModelAuthors = (DefaultTableModel) jTable1.getModel();
        tableModelAuthors.setRowCount(0);
        List listAuthor = new ArrayList();
        listAuthor = ctrAuthor.carregarAuthors();
        if (listAuthor != null && !listAuthor.isEmpty()) {
            Iterator i = listAuthor.iterator();
            while (i.hasNext()) {
                author = (Author) i.next();
                tableModelAuthors.addRow(new Object[] {author.getAuthor_id(), author.getName(),author.getFname() });
            }
        }else{
            JOptionPane.showMessageDialog(null, "Nenhum Autor disponível para listar!!");
        }
    }
    
    private void jBNovoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jBNovoMouseClicked
        author = new Author();
        author.setAuthor_id(Integer.parseInt(jTfName1.getText()));
        author.setName(jTfName.getText());
        author.setFname(jTfFullName.getText());        
        if (ctrAuthor.alterarAuthor(author) == 1) {
            this.listAuthors();
            JOptionPane.showMessageDialog(null, "Autor  atualizado com sucesso!!");
        } else {
            JOptionPane.showMessageDialog(null, "Não foi possível atualizar o autor");
        }
    }//GEN-LAST:event_jBNovoMouseClicked

